function bubbleSort(arr) {
    let len = arr.length;
    for (let i = 0; i < len; i++) {
        for (let j = 0; j < len - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                // Swap arr[j] and arr[j + 1]
                let temp = arr[j];
                arr[j] = arr[j + 1];
                arr[j + 1] = temp;
            }
        }
    }
    return arr;
}

// Usage
let arr = [64, 34, 25, 12, 22, 11, 90];
console.log("Unsorted array is: ", arr);
console.log("Sorted array is: ", bubbleSort(arr));

